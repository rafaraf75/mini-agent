/**
 * Sums two numbers.
 *
 * @param {number} a - The first number to add.
 * @param {number} b - The second number to add.
 * @returns {number} The sum of {@code a} and {@code b}.
 */
function sum(a, b) {
  return a + b;
}

/**
 * Greets a person by name.
 *
 * @param {string} name - The name of the person to greet.
 * @returns {string} A greeting message for the specified name.
 */
function greet(name) {
  return `Witaj, ${name}!`;
}